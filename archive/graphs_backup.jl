# ╔═╡ 8e9b8b9c-fb40-4a5c-bddb-b801ff8078d2
function n_vertices(G::TropicalCurve)
	return size(G.adj_matrix, 1)
end

# ╔═╡ 4cf3a8d1-f7c8-405a-9f48-92039c4facfe
function add_vertices!(G::TropicalCurve, n)
	G.adj_matrix = blockdiag(G.adj_matrix, spzeros(n, n))
end

# ╔═╡ a25b1d8c-dd98-49a3-aa5d-c2027fd69bfd
function add_edge!(G::TropicalCurve, v1::Vertex, v2::Vertex)
	G.adj_matrix[v1, v2] += 1
	G.adj_matrix[v2, v1] += 1
end

# ╔═╡ 8d4f6a9b-4796-4720-a9c0-a8d6b8995111
function remove_edge!(G::TropicalCurve, v1::Vertex, v2::Vertex)
	if G.adj_matrix[v1, v2] <= 0
		return false
	else
		G.adj_matrix[v1, v2] -= 1
		G.adj_matrix[v2, v1] -= 1
		dropzeros!(G.adj_matrix)
		return true
	end
end

# ╔═╡ b49575a6-cb0f-44b1-9f92-d49e8c254e57
function firing_matrix(G::TropicalCurve)
	valences = sum(G.adj_matrix, dims=1)
	return G.adj_matrix - spdiagm(0 => vec(valences))
end

# ╔═╡ ed442fb5-16fc-4827-81b5-b13f8e64230b
function vertex_dists(G::TropicalCurve, v::Vertex)
	n = size(G.adj_matrix, 1)
	
	# find vertex distance from base using BFS
	q = Queue{Vertex}()
	enqueue!(q, v)
	dist = fill(-1, n)
	dist[v] = 0
	while length(q) > 0
		u = dequeue!(q)
		(neighbors, val) = findnz(G.adj_matrix[:, u])
		for nbh in neighbors[val .> 0]
			if dist[nbh] == -1 # if vertex was not visited
				dist[nbh] = max(0, dist[u]) + 1
				enqueue!(q, nbh)
			end
		end
	end
	return dist
end

# ╔═╡ 4ff161cb-db71-443b-8816-beefa44d5a1b
function reduce(G::TropicalCurve, D::Divisor, v::Vertex)
	n = size(G.adj_matrix, 1)
	
	f = firing_matrix(G)

	dist = vertex_dists(G, v)

	# fire subgraphs in layers to obtain a divisor effective away from v
	currdiv = copy(D)
	currdist = maximum(dist)
	while (m = minimum(currdiv[dist .>= currdist])) < 0 && currdist > 0
		currdiv -= f * (dist .< currdist) * m
		currdist -= 1
	end

	level_map = zeros(Int64, n)
	# perform Dhar's burning algorithm
	while true
		q = Queue{Vertex}()
		enqueue!(q, v)
		burned = fill(false, n)
		burned[v] = true
		divcopy = copy(currdiv)
		while length(q) > 0
			u = dequeue!(q)
			(neighbors, val) = findnz(G.adj_matrix[:, u])
			for nbh in neighbors[val .> 0]
				if !burned[nbh]
					divcopy[nbh] -= G.adj_matrix[u, nbh]
					if divcopy[nbh] < 0
						burned[nbh] = true
						enqueue!(q, nbh)
					end
				end
			end
		end

		if all(burned)
			break
		end

		currdiv += f * .!burned
		level_map += .!burned
	end

	return currdiv, level_map
end

# ╔═╡ 0f2c3b22-1886-4078-be5d-8931a6ffdba0
function canonical(G::TropicalCurve)
	valences = vec(sum(G.adj_matrix, dims=1))
	return valences .- 2
end

# ╔═╡ 723f4022-b05a-46b2-9f6d-7e2e13bff363
function is_canonical(G::TropicalCurve, D::Divisor)
	K = canonical(G)
	return reduce(G, K, 1)[1] == reduce(G, D, 1)[1]
end

# ╔═╡ cdf55264-b7c4-417b-8a25-364e1d1f9ed0
function get_level_map(G::TropicalCurve, ref::Divisor, D::Divisor)
	r1, l1 = reduce(G, ref, 1)
	r2, l2 = reduce(G, D, 1)
	if r1 != r2
		return nothing
	end
	return l1 - l2
end

# ╔═╡ 9424ed85-5345-44ea-9a57-70a77b999fe9
function n_edges_between(G::TropicalCurve, v1::Vertex, v2::Vertex)
	return div(G.adj_matrix[v1, v2], v1 == v2 ? 2 : 1)
end

