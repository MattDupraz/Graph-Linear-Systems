# ╔═╡ 6bf0055a-26d6-4e59-96e7-88d4aa104983
function plotcurve(G::TropicalCurve; 
		labels::Vector=[],
		nodesize=0.1,
		curves=true,
		color="white",
		seed=1,
		weights::Vector=[])
	n = size(G.adj_matrix, 1)
	if isempty(labels)
		labels = 1:n
	end
	if isempty(weights)
		weights = labels .> 0
	end
	graphplot(adjlist(G, no_duplicates=true);
		names=labels != nothing ? labels : 1:n,
		nodecolor=color, 
		self_edge_size=0.2,
		arrow=false,
		nodeshape=:circle,
		curves=curves,
		rng=Graphs.rng_from_rng_or_seed(nothing, seed),
		curvature=((G.adj_matrix .> 1) .| spdiagm(0=>fill(1, n))) .* 0.05,
		node_weights=weights,
		nodesize=nodesize)
end

